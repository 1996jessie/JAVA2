import java.awt.Frame;

public class Ex01_Frame {
	public static void main(String[] args) {
		
		Frame f = new Frame(); //프레임 생성
		f.setSize(400, 300);
		f.setLocation(100, 300);
		f.setTitle("프레임생성"); //객체 생성할 때 문자열 넣어도 됨
		f.setVisible(true); //프레임 보이게 설정
		
		
		
		
	}
}

